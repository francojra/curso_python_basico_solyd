class Cliente:
  def __init__(self, nome, idade, empresa, salario): # Método construtor, constroi o objeto
      self.nome = nome
      self.idade = idade
      self.empresa = empresa
      self.salario = salario

### Aumento no salário:

def ganho(self, aumento):
    self.salario = salario + aumento # Vai somar o salário com o aumento (ganho)

