class Cliente:
  def __init__(self, nome, idade, empresa, salario): # Método construtor, constroi o objeto
      self.nome = nome
      self.idade = idade
      self.empresa = empresa
      self.salario = salario

### Aumento no salário:

def aumento(self, ganho):
    self.salario = salario + ganho # Vai somar o salário com o aumento (ganho)
